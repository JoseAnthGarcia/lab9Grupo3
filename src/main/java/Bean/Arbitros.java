package Bean;

public class Arbitros {

    private String nombre;
    private String pais;
    private int idArbitros;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public int getIdArbitros() {
        return idArbitros;
    }

    public void setIdArbitros(int idArbitros) {
        this.idArbitros = idArbitros;
    }
}